    boolean checkinput(Object... args) {
        for (Object txtinput : args) {
            try {
                String txt = ((JTextField) txtinput).getText().trim();
                if (txt.equals("") || txt.equals(" ")) {
                    Message.alert(this, "Không được để trống!!!!");
                    return false;
                }
            } catch (Exception e) {
            }
        }
        return true;
    }
    boolean check(){
        return checkinput(txtGiam, txtGiamToiDa, txtHoaDonToiThieu, txtSoLuong,txtMaGG)&&checkDulieu();
    }
    boolean checkDulieu() {
        boolean result = true;

        try {
            int giam = Integer.parseInt(txtGiam.getText());
            int hoadontoithieu = Integer.parseInt(txtHoaDonToiThieu.getText());
            int giamtoida = Integer.parseInt(txtGiamToiDa.getText());
            if (((LoaiGiamGia) cboLoaiGiamGia.getSelectedItem()).getLoaiGG().trim().equals("PHAMTRAM")) {
                if (giam >= 100 || giam <= 0) {
                    Message.alert(this,"Giảm % không dc nhỏ hơn 0");
                    result = false;
                }
            } else if (((LoaiGiamGia) cboLoaiGiamGia.getSelectedItem()).getLoaiGG().trim().equals("TIEN")) {
                if (giam<0||giamtoida<0||hoadontoithieu<0) {
                    Message.alert(this,"Giảm, Giảm tối đa, Giảm tối thiệu phải >=0");
                    result = false;
                }
            }
        } catch (NumberFormatException e) {
            Message.alert(this,"Giảm, Giảm tối đa, Giá trị tối thiểu phải là số nguyên");
            return false;
        }
        return result;
    }

    void filltable() {
        DefaultTableModel tablemodel = (DefaultTableModel) tblMaGiamGia.getModel();
        tablemodel.setRowCount(0);
        List<PhieuGiamGia> list = pggdao.selectAll();
        for (PhieuGiamGia pgg : list) {
            Object[] row = new Object[7];
            row[0] = pgg.getMapgg();
            row[1] = pgg.getLoaigg();
            row[2] = pgg.getGiam();
            row[3] = pgg.getGiatritoithieu();
            row[4] = pgg.getGiamtoida();
            row[5] = pgg.isTinhtrang()? "Bật":"Tắt";
            row[6] = pgg.getSoluong();
            tablemodel.addRow(row);
        }
    }

    void init() {
        fillComBoBoxLoaiGG();
        filltable();
    }
    PhieuGiamGiaDAO pggdao = new PhieuGiamGiaDAO();
    LoaiGiamGiaDAO lggdao = new LoaiGiamGiaDAO();

    void fillComBoBoxLoaiGG() {
        List<LoaiGiamGia> list = lggdao.SelectALL();
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLoaiGiamGia.getModel();
        model.removeAllElements();
        for (LoaiGiamGia lgg : list) {
            model.addElement(lgg);
        }
        cboLoaiGiamGia.setSelectedIndex(0);
    }

    void clearForm() {
        setForm(new PhieuGiamGia());
    }

    void setForm(PhieuGiamGia hh) {
        txtMaGG.setText(hh.getMapgg());
        txtGiamToiDa.setText(String.valueOf(hh.getGiamtoida()));
        txtHoaDonToiThieu.setText(String.valueOf(hh.getGiatritoithieu()));
        txtGiam.setText(String.valueOf(hh.getGiam()));
        txtSoLuong.setText(String.valueOf(hh.getSoluong()));
        chkTrangThai.setSelected(hh.isTinhtrang());
        if (hh.getLoaigg() != null) {
            cboLoaiGiamGia.setSelectedItem(new LoaiGiamGia(hh.getLoaigg()));
        }
        editable(txtGiamToiDa, txtHoaDonToiThieu, txtGiam, txtSoLuong);
        btnsetable(btnSua, btnXoa, btnMoi);
    }

    PhieuGiamGia getForm() {
        PhieuGiamGia hh = new PhieuGiamGia();
        hh.setMapgg(txtMaGG.getText());
        LoaiGiamGia lh = (LoaiGiamGia) cboLoaiGiamGia.getSelectedItem();
        hh.setLoaigg(lh.getLoaiGG());
        hh.setGiam(Integer.parseInt(txtGiam.getText()));
        hh.setGiamtoida(Integer.parseInt(txtGiamToiDa.getText()));
        hh.setGiatritoithieu(Integer.parseInt(txtHoaDonToiThieu.getText()));
        hh.setSoluong(Integer.parseInt(txtSoLuong.getText()));
        hh.setTinhtrang(chkTrangThai.isSelected());
        return hh;
    }

