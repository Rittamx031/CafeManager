    void Them() {
        PhieuGiamGia hh = getForm();
        if (!check()) {
            Message.alert(this, "Kiểm Tra Đầu Vào");
        } else {
            try {
                pggdao.insert(hh);
                this.filltable();
                this.clearForm();
                Message.alert(this, "Thêm thành công!");
            } catch (Exception e) {
                Message.alert(this, "Thêm thất bại!");
                System.out.println(e);
            }
        }
    }

    void xoa() {
        PhieuGiamGia hh = getForm();
        if (hh.getMapgg().trim().equals("")) {
            Message.alert(this, "Chọn phiếu để xóa!");
        } else {
            if (Message.confirm(this, "Bạn muốn xóa " + "\"" + hh.getMapgg() + "\"" + " chứ?")) {
                try {
                    pggdao.delete(hh.getMapgg());
                    this.filltable();
                    this.clearForm();
                    Message.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    Message.alert(this, "Xóa thất bại!");
                }
            }
        }
    }

    void sua() {
        PhieuGiamGia hh = getForm();
        if (!check()) {
            Message.alert(this, "Chưa chọn Phiếu GG!!!");
        } else {
            PhieuGiamGia hh1 = pggdao.SelectById(hh.getMapgg().trim());
            if (Message.confirm(this, "Bạn muốn Sửa " + "\"" + hh.getMapgg() + "\"" + " trong kho chứ?"
                    + "\n Giảm: " + hh1.getGiam() + "  " + hh1.getLoaigg() + " --> " + hh.getGiam() + "  " + hh.getLoaigg()
                    + "\n Giá Trị Tối Thiểu " + String.valueOf(hh1.getGiatritoithieu()) + " --> " + String.valueOf(hh.getGiatritoithieu())
                    + "\n Giảm tối đa :" + hh1.getGiamtoida() + "--->" + hh.getGiamtoida()
                    + "\n Tình Trạng sản phẩm " + (hh.isTinhtrang() ? "Có thể sử dụng" : "tắt")
            )) {
                try {
                    pggdao.update(hh);
                    this.filltable();
                    this.clearForm();
                    Message.alert(this, "Sửa thành công!");
                } catch (Exception e) {
                    Message.alert(this, "Sửa thất bại!");
                    System.out.println(e);
                }
            }
        }
    }

