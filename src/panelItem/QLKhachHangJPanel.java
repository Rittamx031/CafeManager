    void setForm(KhachHang nv) {
        txtMaKH.setText(nv.getMakh());
        txtTenKH.setText(nv.getTenkh());
        txtDiaChi.setText(nv.getDiachi());
        txtSDT.setText(nv.getSdt());
        editable(txtTenKH,txtDiaChi,txtSDT);
    }

    KhachHang getForm() {
        KhachHang nv = new KhachHang();
        nv.setMakh(txtMaKH.getText());
        nv.setTenkh(txtTenKH.getText());
        nv.setSdt(txtSDT.getText());
        nv.setDiachi(txtDiaChi.getText());
        return nv;
    }

    void clearForm() {
        KhachHang nv = new KhachHang();
        this.setForm(nv);
        this.row = -1;
        editable(txtTenKH,txtDiaChi,txtSDT);
//        this.updateStatus();
    }

    void edit() {
        String makh = (String) tblKhachHang.getValueAt(this.row, 0);
        KhachHang nv = nvDAO.SelectById(makh);
        this.setForm(nv);

//        this.updateStatus();
        btnSetOff(btnThem);
    }

    void insert() {
        KhachHang nv = getForm();
        try {
            nvDAO.insert(nv);
            this.fillTable();
            this.clearForm();
            JOptionPane.showMessageDialog(null, "Thêm mới thành công");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Thêm mới thất bại");
        }
    }

    void update() {
        KhachHang nv = this.getForm();
        try {
            nvDAO.update(nv); // cập nhật
            this.fillTable(); // đổ lại bảng
            JOptionPane.showMessageDialog(null, "Cập nhật thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Cập nhật thất bại");
        }

    }

    void delete() {
        String makh = txtMaKH.getText();
        if (txtMaKH.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập mã khách hàng");
        } else {
            int result = JOptionPane.showConfirmDialog(null,
                    "Bạn có chắc muốn xóa thông tin khách hàng này",
                    "Xác nhận",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (result == JOptionPane.YES_OPTION) {
                try {
                    nvDAO.delete(makh);
                    this.fillTable();
                    this.clearForm();

                } catch (Exception e) {
                    e.printStackTrace();

                }
                JOptionPane.showMessageDialog(null, "Đã xóa thành công");

            } else if (result == JOptionPane.NO_OPTION) {
                return;
            } else {

            }

        }

    }

    void search() {
        try {
            if (txtTimKiem.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã khách hàng", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            KhachHang nv = nvDAO.SelectByNAME(txtTimKiem.getText());
            this.setForm(nv);
        } catch (Exception EX) {
            System.out.println(EX);
            JOptionPane.showMessageDialog(null, "Tên khách hàng không tồn tại", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
        }
        
    }
