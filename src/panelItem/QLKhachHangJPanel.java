    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0); // xoá tất cả các hàng trên Jtable
        try {
            List<KhachHang> list = nvDAO.selectAll();
            for (KhachHang nv : list) {
                Object[] row = {nv.getMakh(), nv.getTenkh(), nv.getSdt(), nv.getDiachi()};
                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println();
            JOptionPane.showMessageDialog(null, "Lỗi truy vấn dữ liệu", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
        btnSetOff(btnXoa,btnSua);
    }

